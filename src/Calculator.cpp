/* Calculator.c */
#include "Calculator.h"

#include <stdio.h>

int Add(const int num1, const int num2)
{
    return num1 + num2;
}

int Subtract(const int num1, const int num2)
{
    return num1 - num2;
}

int Multiply(const int num1, const int num2)
{
    return num1 * num2;
}

int Divide(const int num1, const int num2)
{
    return num1 / num2;
}